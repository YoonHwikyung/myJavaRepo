package practice2.model.vo;

public class V40 extends SmartPhone {

	// 필드부
	private String name = "V40";
	private String brand = "LG";
	
	// 기본생성자
	public V40() {}

	// 메소드부
	public String getName() {
		return name;
	}
	
	@Override
	public String information() {
		return name + "는 " + brand + "에서 만들어 졌다.";
	}
	
	@Override
	public void makeacall() {
		System.out.println("번호를 누르고 통화버튼을 누름");
	}

	@Override
	public void takeacall() {
		System.out.println("수신 버튼을 누름");
	}

	@Override
	public void picture() {
		System.out.println("1200, 1600만 트리플 카메라");
	}
	
	@Override
	public void charge() {
		System.out.println("고속 충전");
	}

	@Override
	public void touch() {
		System.out.println("정전식");
	}
	
	@Override
	public void printMaker() {
		System.out.println("LG");
	}


	

	
}
